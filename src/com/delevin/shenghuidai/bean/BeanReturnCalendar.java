package com.delevin.shenghuidai.bean;

public class BeanReturnCalendar {

	private String date_monty_day; // 月日
	private String date_year; // 年
	private String money;// 本金

	private String income;// 收益
	private String desc;// 描述
	private String date_time;// 第几期

	public String getDate_monty_day() {
		return date_monty_day;
	}

	public void setDate_monty_day(String date_monty_day) {
		this.date_monty_day = date_monty_day;
	}

	public String getDate_year() {
		return date_year;
	}

	public void setDate_year(String date_year) {
		this.date_year = date_year;
	}

	public String getMoney() {
		return money;
	}

	public void setMoney(String money) {
		this.money = money;
	}

	public String getIncome() {
		return income;
	}

	public void setIncome(String income) {
		this.income = income;
	}

	public String getDesc() {
		return desc;
	}

	public void setDesc(String desc) {
		this.desc = desc;
	}

	public String getDate_time() {
		return date_time;
	}

	public void setDate_time(String date_time) {
		this.date_time = date_time;
	}

}
